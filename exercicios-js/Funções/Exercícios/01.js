/* Crie uma função que dado dois valores (passados como parâmetros) mostre
no console a soma, subtração, multiplicação e divisão desses valores. */

// FEITO

function operacao(a, b) {
    console.log(`A soma dos valores informados é: ${a + b}`)
    console.log(`A subtração dos valores informados é: ${a - b}`)
    console.log(`A multiplicação dos valores informados é: ${a * b}`)
    console.log(`A divisão dos valores informados é: ${a / b}`)
}

operacao(2, 2)