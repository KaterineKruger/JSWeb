/* Crie uma função que irá receber dois valores, o dividendo e o divisor.
A função deverá imprimir o resultado e o resto da divisão destes dois valores. */

// FEITO

function divisao(dvn, dvs) {
    console.log(`O resultado da divisão dos valores é: ${dvn / dvs}`)
    console.log(`O resto da divisão dos valores é: ${dvn % dvs}`)
}

divisao(10, 3)